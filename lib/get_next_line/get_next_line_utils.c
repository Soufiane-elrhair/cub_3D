#include "get_next_line.h"

void	split_with_nl(char **s1, char **s2, char *s3)
{
	*s1 = ft_substr(s3, 0, ln_index(s3) + 1);
	*s2 = ft_strdup(s3 + ln_index(s3) + 1);
}
